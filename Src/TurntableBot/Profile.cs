namespace TurntableBotSharp
{
    /// <summary>
    /// Class used to store profile information for Turntable.fm
    /// </summary>
    public class Profile
    {
        /// <summary>
        /// Gets or sets the about me field.
        /// </summary>
        /// <value>
        /// The about me field.
        /// </value>
        public string About
        {
            get; set;
        }

        /// <summary>
        /// Gets or sets the facebook field.
        /// </summary>
        /// <value>
        /// The facebook field.
        /// </value>
        public string Facebook
        {
            get; set;
        }

        /// <summary>
        /// Gets or sets the hangout field.
        /// </summary>
        /// <value>
        /// The hangout field.
        /// </value>
        public string Hangout
        {
            get; set;
        }

        /// <summary>
        /// Gets or sets the user name.
        /// </summary>
        /// <value>
        /// The user name.
        /// </value>
        public string Name
        {
            get; set;
        }

        /// <summary>
        /// Gets or sets the top artists field.
        /// </summary>
        /// <value>
        /// The top artists field.
        /// </value>
        public string TopArtists
        {
            get; set;
        }

        /// <summary>
        /// Gets or sets the twitter field.
        /// </summary>
        /// <value>
        /// The twitter field.
        /// </value>
        public string Twitter
        {
            get; set;
        }

        /// <summary>
        /// Gets or sets the website field.
        /// </summary>
        /// <value>
        /// The website field.
        /// </value>
        public string Website
        {
            get; set;
        }
    }
}